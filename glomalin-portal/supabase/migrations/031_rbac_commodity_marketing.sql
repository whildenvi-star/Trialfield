-- ---------------------------------------------------------------------------
-- Step 5: authorize(permission) — one-argument form for RLS policies
--   Reads app_role from the JWT (embedded by custom_access_token_hook).
--   Usage in RLS: USING (public.authorize('contracts.read'))
-- ---------------------------------------------------------------------------
CREATE OR REPLACE FUNCTION public.authorize(
  requested_permission public.app_permission
)
RETURNS boolean
LANGUAGE plpgsql
STABLE
SECURITY DEFINER
SET search_path = ''
AS $$
DECLARE
  bind_permissions int;
  user_role        public.app_role;
BEGIN
  -- Read app_role from JWT — embedded at app_metadata.app_role by the hook
  SELECT (auth.jwt()->'app_metadata'->>'app_role')::public.app_role
  INTO user_role;

  SELECT COUNT(*)
  INTO bind_permissions
  FROM public.role_permissions
  WHERE role_permissions.permission = requested_permission
    AND role_permissions.role = user_role;

  RETURN bind_permissions > 0;
END;
$$;

GRANT EXECUTE ON FUNCTION public.authorize(public.app_permission) TO authenticated;


