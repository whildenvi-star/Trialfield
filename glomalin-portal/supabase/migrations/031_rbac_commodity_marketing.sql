-- ---------------------------------------------------------------------------
-- Step 7: custom_access_token_hook(event jsonb) — JWT enrichment hook
--   Reads raw_app_meta_data->>'app_role' from auth.users and embeds it
--   into the JWT claims under app_metadata.app_role.
--
--   Grant: supabase_auth_admin only.
--   Revoke: authenticated, anon, public (security: no user can call this directly).
--
--   After running: register in Dashboard → Authentication → Hooks → Custom Access Token.
-- ---------------------------------------------------------------------------
CREATE OR REPLACE FUNCTION public.custom_access_token_hook(event jsonb)
RETURNS jsonb
LANGUAGE plpgsql
AS $$
DECLARE
  claims        jsonb;
  app_role_val  text;
BEGIN
  -- Read app_role from raw_app_meta_data on auth.users
  -- raw_app_meta_data is writeable only by service-role admin — users cannot self-elevate
  SELECT raw_app_meta_data->>'app_role'
  INTO app_role_val
  FROM auth.users
  WHERE id = (event->>'user_id')::uuid;

  claims := event->'claims';

  IF app_role_val IS NOT NULL THEN
    -- Ensure app_metadata key exists in claims
    IF jsonb_typeof(claims->'app_metadata') IS NULL THEN
      claims := jsonb_set(claims, '{app_metadata}', '{}');
    END IF;
    -- Embed app_role into app_metadata
    claims := jsonb_set(claims, '{app_metadata,app_role}', to_jsonb(app_role_val));
  END IF;

  event := jsonb_set(event, '{claims}', claims);
  RETURN event;
END;
$$;

GRANT EXECUTE ON FUNCTION public.custom_access_token_hook TO supabase_auth_admin;
REVOKE EXECUTE ON FUNCTION public.custom_access_token_hook FROM authenticated, anon, public;
